// 题目
/*
并发和并行
*/

// 解答
/*
并发（concurrency）：
   指宏观上看起来两个程序在同时运行，比如说在单核cpu上的多任务。
   但是从微观上看两个程序的指令是交织着运行的，你的指令之间穿插着我的指令，我的指令之间穿插着你的，在单个周期内只运行了一个指令。
   这种并发并不能提高计算机的性能，只能提高效率。
并行（parallelism）：
   指严格物理意义上的同时运行，比如多核cpu，两个程序分别运行在两个核上，两者之间互不影响，
   单个周期内每个程序都运行了自己的指令，也就是运行了两条指令。
   这样说来并行的确提高了计算机的效率。所以现在的cpu都是往多核方面发展。
*/

