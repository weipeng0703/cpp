// 题目
/*
数字证书是什么，里面都包含那些内容
*/

// 解答
/*
1）概念：
数字证书是数字证书在一个身份和该身份的持有者所拥有的公/私钥对之间建立了一种联系，
由认证中心（CA）或者认证中心的下级认证中心颁发的。

根证书是认证中心与用户建立信任关系的基础。在用户使用数字证书之前必须首先下载和安装。

认证中心是一家能向用户签发数字证书以确认用户身份的管理机构。为了防止数字凭证的伪造，认证中心的公共密钥必须是可靠的，认证中心必须公布其公共密钥或由更高级别的认证中心提供一个电子凭证来证明其公共密钥的有效性，后一种方法导致了多级别认证中心的出现。

2）数字证书颁发过程：

    1.用户产生了自己的密钥对，并将公共密钥及部分个人身份信息传送给一家认证中心。
    2.认证中心在核实身份后，将执行一些必要的步骤，以确信请求确实由用户发送而来。
    3.然后，认证中心将发给用户一个数字证书，该证书内附了用户和他的密钥等信息，
    同时还附有对认证中心公共密钥加以确认的数字证书。
    当用户想证明其公开密钥的合法性时，就可以提供这一数字证书。

3）内容：

数字证书的格式普遍采用的是X.509V3国际标准，一个标准的X.509数字证书包含以下一些内容：

1、证书的版本信息；

2、证书的序列号，每个证书都有一个唯一的证书序列号；

3、证书所使用的签名算法；

4、证书的发行机构名称，命名规则一般采用X.500格式；

5、证书的有效期，通用的证书一般采用UTC时间格式；

6、证书所有人的名称，命名规则一般采用X.500格式；

7、证书所有人的公开密钥；

8、证书发行者对证书的签名。

*/

