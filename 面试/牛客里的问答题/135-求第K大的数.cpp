// 题目
/*
1.求第k大的数的方法以及各自的复杂度是怎样的
2.当有相同元素时，还可以使用什么不同的方法求第k大的元素
*/

// 解答
/*
使用快速排序算法将数组按照从大到小排序，然后取第k个，其时间复杂度最快为O(nlogn)

使用堆排序，建立最大堆，然后调整堆，知道获得第k个元素，其时间复杂度为O(n+klogn)

首先利用哈希表统计数组中个元素出现的次数，然后利用计数排序的思想，线性从大到小扫描过程中，前面有k-1个数则为第k大的数

利用快排思想，从数组中随机选择一个数i，然后将数组分成两部分Dl,Dr，Dl的元素都小于i,Dr的元素都大于i。
然后统计Dr元素个数，如果Dr元素个数等于k-1,那么第k大的数即为k，
如果Dr元素个数小于k,那么继续求Dl中第k-Dr大的元素；
如果Dr元素个数大于k,那么继续求Dr中第k大的元素。


当有相同元素的时候，

首先利用哈希表统计数组中各元素出现的次数，然后利用计数排序的思想，线性从大到小扫描过程中，
前面有k-1个数则为第k大的数，平均情况下时间复杂度为O(n)
*/