// 题目
/*
异步编程的事件循环
*/

// 解答
/*

事件循环就是不停循环等待时间的发生，然后将这个事件的所有处理器，以及他们订阅这个事件的时间顺序依次依次执行。
当这个事件的所有处理器都被执行完毕之后，事件循环就会开始继续等待下一个事件的触发，不断往复。
当同时并发地处理多个请求时，以上的概念也是正确的。

可以这样理解：在单个的线程中，事件处理器是一个一个按顺序执行的。
即如果某个事件绑定了两个处理器，那么第二个处理器会在第一个处理器执行完毕后，才开始执行。
在这个事件的所有处理器都执行完毕之前，事件循环不会去检查是否有新的事件触发。
在单个线程中，一切都是有顺序地一个一个地执行的！

*/

